package Normal_Codes;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 30;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        double x = 5;
        double y = 6;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);

    }
}
